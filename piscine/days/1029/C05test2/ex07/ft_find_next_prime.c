int	ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 2)
		return (2);
	while (i < nb)
	{
		if (nb % i == 0)
		{
			nb = nb + 1;
			i = 1;
		}
		i++;
		if (i == nb)
			return (nb);
	}
	return (0);
}
